#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t		i;
	size_t		j;
	size_t		h;

	i = ft_strlen(dst);
	h = ft_strlen(src);
	j = 0;
	if (size <= i)
		h = h + size;
	else
		h = h + i;
	while (src[j] && i + 1 < size)
	{
		dst[i] = src[j];
		i++;
		j++;
	}
	dst[i] = '\0';
	return (h);
}
