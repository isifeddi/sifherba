char	*ft_strcat(char *s1, const char *s2)
{
	int				i;
	int				j;
	const char		*p;

	i = 0;
	j = 0;
	p = s2;
	while (s1[i] != '\0')
		i++;
	while (p[j] != '\0')
	{
		s1[i] = p[j];
		i++;
		j++;
	}
	s1[i] = '\0';
	return (s1);
}
