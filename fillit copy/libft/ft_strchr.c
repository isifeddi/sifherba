#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char		*p;
	int			i;

	p = (char *)s;
	i = ft_strlen(p);
	if (c == '\0')
	{
		return (p + i);
	}
	while (*p != '\0')
	{
		if (*p == c)
			return (p);
		p++;
	}
	return (0);
}
